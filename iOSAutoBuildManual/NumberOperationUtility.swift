import Foundation


class NumberOperationUtility {
    
    
    func addTwoNumbers(firstNumber:Int , secondNumber:Int) -> Int {
        return firstNumber + secondNumber
    }
    
    func multiplyTwoNumbers(firstNumber:Int , secondNumber:Int) -> Int {
        return firstNumber * secondNumber
    }
    
}
